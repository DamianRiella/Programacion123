/* Funciones de SUMA, RESTA, MULTIPLICACION, DIVICION, FACTORIAL*/

int factorial(int num)
{
    int valor, resultado=1, i;

    for(i=valor; i>0; i--)
    {
        resultado= resultado*i;
        return resultado;
    }

}

float suma(float num1, float num2)
{
    int resultado;
    resultado= num1 + num2;
    return resultado;
}

float resta(float num1, float num2)
{
    int resultado;
    resultado= num1 - num2;
    return resultado;
}

float multiplicacion(float num1, float num2)
{
    int resultado;
    resultado= num1 * num2;
    return resultado;
}
float division(float num1, float num2)
{
    int resultado;
    resultado= num1 / num2;
    return resultado;
}
